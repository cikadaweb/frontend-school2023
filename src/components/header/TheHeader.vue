<template>
  <header class="header">
    <div class="container">
      <nav class="header__nav">
        <ul class="header__list">
          <li v-for="link in navLinks" :key="link.id">
            <router-link class="header__link" :to="link.url">{{ link.name }}</router-link>
          </li>
        </ul>
        <div class="header__profile" :class="isActiveProfile" @click="toggleProfileMenu" ref="profileButton">
          <img
            class="user-avatar"
            src="@/assets/img/avatar.png"
            alt="profile-img"
          />
          <div class="header__arrow-icon" :class="isShowDropdown ? 'header__arrow-icon_active' : ''">
            <AppIcon id="#drop_down_1" width="24" height="24" />
          </div>

          <AppDropdown
            class="header__dropdown"
            :isShowDropdown="isShowDropdown"
            :items="dropdownList"
            ref="profileDropdown"
          />

        </div>
      </nav>
    </div>
  </header>
</template>

<script>

export default {
  name: 'TheHeader',
  mounted () {
    document.addEventListener('click', this.handleOutsideClick)
  },
  beforeDestroy () {
    document.removeEventListener('click', this.handleOutsideClick)
  },
  data () {
    return {
      navLinks: [
        {
          id: 1,
          name: 'Проекты',
          url: '/projects'
        },
        {
          id: 2,
          name: 'Задачи',
          url: '/tasks'
        },
        {
          id: 3,
          name: 'Пользователи',
          url: '/users'
        }
      ],
      dropdownList: [
        {
          id: 1,
          text: 'Профиль',
          clickHandler: this.goToProfile
        },
        {
          id: 2,
          text: 'Выход',
          clickHandler: this.goToExit
        }
      ],
      isShowDropdown: false
    }
  },
  methods: {
    toggleProfileMenu () {
      this.isShowDropdown = !this.isShowDropdown
    },
    handleOutsideClick (event) {
      const profileButton = this.$refs.profileButton
      const profileDropdown = this.$refs.profileDropdown.$el
      if (
        profileButton &&
        profileDropdown &&
        !profileButton.contains(event.target) &&
        !profileDropdown.contains(event.target)
      ) {
        this.isShowDropdown = false
      }
    },
    goToProfile () {
      if (this.$route.path !== '/profile') {
        this.$router.push('/profile')
      }
    },
    goToExit () {
      console.log('Выйти из профиля')
    }
  },
  computed: {
    isActiveProfile () {
      if (this.isShowDropdown) {
        return 'header__profile_active'
      }
      return ''
    }
  }
}
</script>

